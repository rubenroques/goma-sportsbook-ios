import Foundation

extension Array {
    var isNotEmpty: Bool {
        return !isEmpty
    }
}

extension String {
    var isNotEmpty: Bool {
        return !isEmpty
    }
}

extension Dictionary {
    var isNotEmpty: Bool {
        return !isEmpty
    }
}

extension Set {
    var isNotEmpty: Bool {
        return !isEmpty
    }
}
